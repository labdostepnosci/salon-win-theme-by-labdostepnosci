# Instrukcja importu postów do WordPressa

Plik importu:

`salon-win-posty-import.wordpress.xml`

## Jak zaimportować

1. Wejdź w panel WordPress.
2. Przejdź do `Narzędzia -> Import`.
3. Wybierz importer `WordPress`.
4. Jeśli importer nie jest zainstalowany, kliknij `Zainstaluj teraz`, potem `Uruchom importer`.
5. Wgraj plik `salon-win-posty-import.wordpress.xml`.
6. Przy przypisaniu autora wybierz istniejącego użytkownika, np. administratora strony.
7. Opcja pobierania załączników nie ma znaczenia, bo wpisy nie zawierają obrazków.
8. Po imporcie przejdź do `Wpisy -> Wszystkie wpisy`.

## Co zostanie dodane

- 12 pełnych wpisów blogowych.
- Status wpisów: `Szkic`.
- Kategorie wpisów.
- Slugi URL.
- Zajawki wpisów.
- Treść w prostym HTML/Gutenberg blocks.
- Meta title i meta description dla Yoast SEO oraz Rank Math.

## Po imporcie

Warto przejrzeć każdy szkic, dodać obrazek wyróżniający i dopiero wtedy publikować.

Jeśli chcesz, żeby wpisy pojawiały się na stronie głównej, motyw musi mieć sekcję pokazującą najnowsze wpisy albo wybrane kategorie.

<p>Digital detox nie musi oznaczać surowego zakazu telefonu ani wyjazdu na koniec świata. Czasem wystarczy miejsce, które zachęca do tego, żeby ekran przestał być centrum dnia. Miejsce, w którym naturalnie zaczynasz słyszeć rozmowę, dźwięk kroków, szum drzew, nalewaną wodę, ciszę między zdaniami.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>W Salon Win digital detox rozumiemy jako weekend oddechu od przebodźcowania. To propozycja dla osób, które dużo pracują, stale odpowiadają na wiadomości, żyją między spotkaniami, powiadomieniami i kalendarzem. Pobyt ma dać poczucie miękkiego resetu: bez presji, bez krzykliwej "promocji", bez planu wypełnionego po brzegi.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Taki weekend może zacząć się od spokojnej kolacji. Potem telefon można zostawić w pokoju albo wyciszyć. Następnego dnia zamiast scrollowania - śniadanie, spacer, sauna, praca z oddechem, cicha lektura, kontakt ze sztuką lub kameralny warsztat. Wieczorem: rozmowa, dobra kuchnia, ogród, światło i czas, który nie jest przerywany co kilka minut.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>To oferta szczególnie bliska osobom, które czują, że potrzebują regeneracji, ale nie odnajdują się w hałaśliwych formach wypoczynku. Digital detox w Jaśle nie jest karą za korzystanie z technologii. Jest zaproszeniem do odzyskania proporcji.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>W prezentacji strategii pojawia się komunikacja "ASMR": szum drzew, dźwięk nalewanego wina, kroki w piwnicy, detale, światło, cisza. To dobry kierunek także dla strony. Sprzedajemy nie tylko nocleg, ale stan po pobycie: spokojniejszą głowę, ciało bez napięcia i poczucie, że można wrócić do codzienności lżej.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->

<p>Taki pobyt może być ofertą dla grup kobiecych, małych społeczności, instruktorek jogi, trenerek oddechu, coachów, terapeutek pracy z ciałem albo prywatnych grup przyjaciółek. Salon Win zapewnia przestrzeń, nocleg, kuchnię i atmosferę; prowadzący mogą wypełnić ją własnym programem.</p>
